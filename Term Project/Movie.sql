create table movie(id int not null primary key, title varchar(50), year int, score numeric(3,2), running_time int, rating varchar(10));
create table country(id int not null primary key, country_name varchar(10));
create table country_movie(country_id int, movie_id int, foreign key (country_id) references country (id), foreign key (movie_id) references movie (id), primary key (country_id, movie_id));
create table director(id int not null primary key, name varchar(30));
create table makes(director_id int, movie_id int, foreign key (director_id) references director (id), foreign key (movie_id) references movie (id), primary key (director_id, movie_id));
create table writer(id int not null primary key, name varchar(30));
create table writes(writer_id int, movie_id int, foreign key (writer_id) references writer (id), foreign key (movie_id) references movie (id), primary key (writer_id, movie_id));
create table actor(id int not null primary key, name varchar(30));
create table plays(actor_id int, movie_id int, role varchar(30), foreign key (actor_id) references actor (id), foreign key (movie_id) references movie (id), primary key (actor_id, movie_id));
create table genre(id int not null primary key, name varchar(20));
create table genre_movie(genre_id int, movie_id int, foreign key (genre_id) references genre (id), foreign key (movie_id) references movie (id), primary key (genre_id, movie_id));
create table users(id varchar(20) not null primary key, pw varchar(20) not null, age int default 15);
create table users_pick(user_id varchar(20) not null, movie_id int, score numeric(3,2), foreign key (user_id) references users (id), foreign key (movie_id) references movie (id), primary key (user_id, movie_id));



